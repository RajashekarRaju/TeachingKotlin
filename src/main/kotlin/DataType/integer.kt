package DataType

/**
 * Integer Datatype
 */

fun main(){

    //Declaring Byte
    val byteValue: Byte = 1

    //Declaring Short
    val shortValue: Short = 32

    //Declaring Integer
    val integerValue: Int = 1012

    //Declaring Long
    val LongValue: Long = 1010121211

    //Declaring Long Adding suffix L to the value
    val LongNumber:Long = 10L

    println("Print the value of byteValue :$byteValue")
    println("Print the value of shortValue :$shortValue")
    println("Print the value of integerValue :$integerValue")
    println("Print the value of LongValue :$LongValue")
    println("Print the value of LongNumber :$LongNumber")
}