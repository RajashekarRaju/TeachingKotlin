package DataType

/**
 * Float Datatype
 * Floating point data type is used to store decimal values.
 * Kotlin provide Floating point data type Float and Double.
 * In Kotlin,it will took Double as default data type for decimal values.
 */
fun main(){

    //Declaring Float by add suffix F to value.
    val floatValue:Float = 1.56F

    //Declaring Double
    val doubleValue:Double = 12.12121212

    println("Print the value of floatValue : $floatValue")
    println("Print the value of doubleValue : $doubleValue")
}